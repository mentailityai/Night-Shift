# Dispatch table mapping provider names to their handler functions
_PROVIDER_DISPATCH = {
    "vllm": _call_vllm,
    "openai": _call_openai,
    "anthropic": _call_anthropic,
}


async def analyse_interaction(
    system_prompt: str,
    user_input: str,
    ai_output: str,
    human_correction: str,
) -> str:
    """
    Send a raw interaction to the configured LLM provider for analysis.

    This is the main entry point used by the worker's ``agent.py``.
    It reads the active provider from the config, formats the user
    message, and dispatches to the appropriate handler.

    Parameters
    ----------
    system_prompt : str
        The original instructions given to the drafting AI.
    user_input : str
        The user request or uploaded source text.
    ai_output : str
        The AI-generated response (before edits).
    human_correction : str
        The final text after the user's manual edits.

    Returns
    -------
    str
        Raw text response from the LLM (expected to be valid JSON).

    Raises
    ------
    ValueError
        If the configured ``LLM_PROVIDER`` is not supported.
    """
    settings = get_settings()
    provider = settings.llm_provider

    handler = _PROVIDER_DISPATCH.get(provider)
    if handler is None:
        raise ValueError(
            f"Unsupported LLM provider: '{provider}'. "
            f"Supported: {list(_PROVIDER_DISPATCH.keys())}"
        )

    # Build the formatted user message from the raw interaction fields
    user_message = _build_user_message(
        system_prompt=system_prompt,
        user_input=user_input,
        ai_output=ai_output,
        human_correction=human_correction,
    )

    # Dispatch to the appropriate provider
    raw_response = await handler(settings, user_message)

    logger.info(
        "llm_response_received",
        provider=provider,
        response_length=len(raw_response),
    )

    return raw_response
